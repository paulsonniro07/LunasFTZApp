const fs = require("fs");
const path = require("path");

// Create minimal valid 1x1 PNG files as placeholders
// These are single-pixel green PNGs that browsers accept as valid
function createMinimalPNG(size) {
  // Minimal valid PNG: 1x1 green pixel
  const png = Buffer.from([
    0x89, 0x50, 0x4e, 0x47, 0x0d, 0x0a, 0x1a, 0x0a, // PNG signature
    0x00, 0x00, 0x00, 0x0d, 0x49, 0x48, 0x44, 0x52, // IHDR chunk
    0x00, 0x00, 0x00, 0x01, 0x00, 0x00, 0x00, 0x01, // 1x1 pixel
    0x08, 0x02, 0x00, 0x00, 0x00, 0x90, 0x77, 0x53, // 8-bit RGB
    0xde, 0x00, 0x00, 0x00, 0x0c, 0x49, 0x44, 0x41, // IDAT chunk
    0x54, 0x08, 0xd7, 0x63, 0xf8, 0xcf, 0xc0, 0x00, // compressed data
    0x00, 0x00, 0x03, 0x00, 0x01, 0x26, 0x2c, 0xed, // ...
    0x59, 0x00, 0x00, 0x00, 0x00, 0x49, 0x45, 0x4e, // IEND chunk
    0x44, 0xae, 0x42, 0x60, 0x82,
  ]);
  return png;
}

const outDir = path.join(__dirname, "..", "public", "icons");
if (!fs.existsSync(outDir)) {
  fs.mkdirSync(outDir, { recursive: true });
}

fs.writeFileSync(path.join(outDir, "icon-192x192.png"), createMinimalPNG(192));
fs.writeFileSync(path.join(outDir, "icon-512x512.png"), createMinimalPNG(512));
console.log("Icons generated successfully!");